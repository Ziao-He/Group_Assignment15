    private void loadProfileData() {
        if (currentRegistrarProfile != null) {
            txtOfficeHour.setText(currentRegistrarProfile.getOfficeHours());
            txtOfficeLocation.setText(currentRegistrarProfile.getOfficeLocation());
            txtPhoneNumber.setText(currentRegistrarProfile.getPhone());
            txtEmailAddress.setText(currentRegistrarProfile.getEmail());
        }
    }

    private void setEditMode() {
        txtEmailAddress.setEnabled(true);
        txtPhoneNumber.setEnabled(true);
        txtOfficeHour.setEnabled(true);
        txtOfficeLocation.setEnabled(true);
        btnEdit.setEnabled(false);
        btnSave.setEnabled(true);
    }

    private void setViewMode() {
        txtEmailAddress.setEnabled(false);
        txtPhoneNumber.setEnabled(false);
        txtOfficeHour.setEnabled(false);
        txtOfficeLocation.setEnabled(false);
        btnEdit.setEnabled(true);
        btnSave.setEnabled(false);
    }
