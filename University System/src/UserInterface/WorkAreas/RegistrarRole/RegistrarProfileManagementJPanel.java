    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            // 1. 获取表单数据
            String officeHours = txtOfficeHour.getText().trim();
            String officeLocation = txtOfficeLocation.getText().trim();
            String phoneNumber = txtPhoneNumber.getText().trim();
            String emailAddress = txtEmailAddress.getText().trim();

            // 2. 数据验证
            if (officeHours.isEmpty()) {
                JOptionPane.showMessageDialog(this, "请输入办公时间", "错误", JOptionPane.ERROR_MESSAGE);
                txtOfficeHour.requestFocus();
                return;
            }

            if (officeLocation.isEmpty()) {
                JOptionPane.showMessageDialog(this, "请输入办公室位置", "错误", JOptionPane.ERROR_MESSAGE);
                txtOfficeLocation.requestFocus();
                return;
            }

            if (phoneNumber.isEmpty()) {
                JOptionPane.showMessageDialog(this, "请输入电话号码", "错误", JOptionPane.ERROR_MESSAGE);
                txtPhoneNumber.requestFocus();
                return;
            }

            if (emailAddress.isEmpty() || !emailAddress.contains("@")) {
                JOptionPane.showMessageDialog(this, "请输入有效的邮箱地址", "错误", JOptionPane.ERROR_MESSAGE);
                txtEmailAddress.requestFocus();
                return;
            }

            // 3. 更新RegistrarProfile信息
            currentRegistrarProfile.setOfficeHours(officeHours);
            currentRegistrarProfile.setOfficeLocation(officeLocation);
            currentRegistrarProfile.setPhone(phoneNumber);
            currentRegistrarProfile.setEmail(emailAddress);

            // 4. 显示成功和失败消息
            JOptionPane.showMessageDialog(this, 
                "个人信息更新成功！\n" +
                "办公时间: " + officeHours + "\n" +
                "办公室: " + officeLocation + "\n" +
                "电话: " + phoneNumber + "\n" +
                "邮箱: " + emailAddress, 
                "更新成功", 
                JOptionPane.INFORMATION_MESSAGE);
            setViewMode();
            } catch (Exception ex) {  
                JOptionPane.showMessageDialog(this, 
                    "保存失败: " + ex.getMessage(), 
                    "错误", 
                    JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
        
    }//GEN-LAST:event_btnSaveActionPerformed

    

